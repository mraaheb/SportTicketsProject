    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
 {
     
    String user = txtUser.getText().trim();
    String pass = txtPass.getText().trim();

    if (user.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter username and password.");
        return;
    }

    try {
        ClientConnection conn = new ClientConnection();
        if (!conn.connect()) { // uses DEFAULT_HOST/DEFAULT_PORT
            JOptionPane.showMessageDialog(this, "Cannot connect to server at "
                + ClientConnection.DEFAULT_HOST + ":" + ClientConnection.DEFAULT_PORT);
            return;
        }

        String resp = conn.sendOneLine(D_Protocol.Protocol.makeRegister(user, pass));
        if (resp != null && resp.startsWith("OK")) {
            JOptionPane.showMessageDialog(this, "Connected & registered âœ…");
            new A_UI.ReservationFrame(conn, user).setVisible(true);
            this.dispose();
        } else if (resp != null && resp.contains("USER_EXISTS")) {
            JOptionPane.showMessageDialog(this, "Username already exists.");
            conn.close();
        } else {
            JOptionPane.showMessageDialog(this, "Failed: " + resp);
            conn.close();
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }}


    }//GEN-LAST:event_btnRegisterActionPerformed

